    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            //Inicio del metodo
            con = c.getConexion();
            stmt = con.createStatement();
            //Sentencia SQL
            ResultSet rs = stmt.executeQuery("SELECT productoSolicitud,cantidadSolicitud,precioSolicitud FROM solicitud WHERE estadoSolicitud = 'Aprobado'");
            modeloAprobado = new DefaultTableModel(null, titulosAprobados);
            while (rs.next()) {
                //Extraemos los valores de la Base de datos
                String NombreP = rs.getString("productoSolicitud");
                System.out.println(NombreP);
                filaAprobacio[0] = NombreP;
                filaAprobacio[1] = rs.getString("cantidadSolicitud");
                filaAprobacio[2] = rs.getString("precioSolicitud");
                int precio, cantidad;
                precio = Integer.valueOf(rs.getString("precioSolicitud"));
                cantidad = Integer.valueOf(rs.getString("cantidadSolicitud"));

                System.out.println(precio);

                filaAprobacio[3] = String.valueOf(cantidad * precio);

                modeloAprobado.addRow(filaAprobacio);
            }
            tableAprobaciones.setModel(modeloAprobado);

        } catch (SQLException e) {
            System.out.println(e.getMessage());

        }
        //Fin del metodo
    }//GEN-LAST:event_btnBuscarActionPerformed

