    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            //Incio del metodo y la conexion 
            con = c.getConexion();
            stmt = con.createStatement();
            //Sentencia SQL
            ResultSet rs = stmt.executeQuery("select* from solicitud");
            modelo = new DefaultTableModel(null, titulos);
            //Aqui agregamos los valores a la tabla
            while (rs.next()) {
                fila[0] = rs.getString("idSolicitud");
                fila[1] = rs.getString("usuarioSolicitud");
                fila[2] = rs.getString("productoSolicitud");
                fila[3] = rs.getString("cantidadSolicitud");
                fila[4] = rs.getString("precioSolicitud");
                fila[5] = rs.getString("estadoSolicitud");
                modelo.addRow(fila);
            }
            tableDetallePedido.setModel(modelo);
            //Aqui van a la tabla
        } catch (Exception e) {
            System.out.println(e.getMessage());

        }
        //Fin del metodo


    }//GEN-LAST:event_btnAgregarActionPerformed

